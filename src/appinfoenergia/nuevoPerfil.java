    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        Perfil perfil = new Perfil();
        perfil.setCorreo_electronico(emailField.getText());
        perfil.setNombres(firstnameField.getText());
        perfil.setApellidos(lastnameField.getText());
        perfil.setCosto_kv(Double.parseDouble(kvField.getText()));
        PerfilDAO p = new PerfilDAO();
        p.perfil_agregar(perfil);
        
    }//GEN-LAST:event_btnSaveActionPerformed

